#There are three numeric types in Python:
#int
#float
#complex

xr=10 #int
yr=20.5 #float
zr=1j #complex
print(type(xr),type(yr),type(zr))

#Int, or integer, is a whole number, positive or negative, without decimals, of unlimited
#length.
x = 1
y = 35656222554887711
z = -3255522
print(type(x))
print(type(y))
print(type(z))

#Float, or "floating point number" is a number, positive or negative, containing one or
#more decimals.
xe= 1.10
ye = 1.0
ze = -35.59
print(type(xe))
print(type(ye))
print(type(ze))

#Float can also be scientific numbers with an "e" to indicate the power of 10.
xt = 35e3
yt = 12E4
zt = -87.7e100
print(type(xt))
print(type(yt))
print(type(zt))

#Complex numbers are written with a "j" as the imaginary part:

xw = 3+5j
yw = 5j
zw = -5j
print(type(xw))
print(type(yw))
print(type(zw))